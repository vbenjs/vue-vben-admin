<script setup lang="ts">
import { Pin, PinOff } from '@vben-core/icons';

const expandOnHover = defineModel<boolean>('expandOnHover');

function toggleFixed() {
  expandOnHover.value = !expandOnHover.value;
}
</script>

<template>
  <div
    class="bg-accent flex-center hover:bg-accent-hover hover:text-foreground text-foreground/60 absolute right-3 bottom-2 z-10 cursor-pointer rounded-sm p-1.25 transition-all duration-300"
    @click="toggleFixed"
  >
    <PinOff v-if="!expandOnHover" class="size-3.5" />
    <Pin v-else class="size-3.5" />
  </div>
</template>
